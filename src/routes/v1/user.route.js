const express = require('express');
const multer = require('multer');
const path = require('path');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const userValidation = require('../../validations/user.validation');
const userController = require('../../controllers/user.controller');

const router = express.Router();

const storage = multer.diskStorage({
  destination(req, file, callback) {
    callback(null, 'src/public/assets/images/users');
  },
  filename(req, file, cb) {
    cb(null, `${new Date().valueOf()}_${file.originalname}`);
  },
});

function checkFileType(file, cb) {
  // Allowed ext
  const filetypes = /jpeg|jpg|png|gif/;
  // Check ext
  const extname = filetypes.test(path.extname(file.originalname).toLowerCase());
  // Check mime
  const mimetype = filetypes.test(file.mimetype);

  if (mimetype && extname) {
    return cb(null, true);
  }
  cb(new Error('Error: Images Only [ jpeg|jpg|png|gif ]!'));
}

const upload = multer({
  storage,
  limits: { fileSize: 2000000 },
  fileFilter(_req, file, cb) {
    checkFileType(file, cb);
  },
}).single('image');

router
  .route('/')
  .post(auth, validate(userValidation.createUser), userController.createUser)
  .get(auth, validate(userValidation.getUsers), userController.getUsers);

router.route('/total').get(auth, validate(userValidation.getTotalUser), userController.getTotalUser);

router.route('/search').post(auth, validate(userValidation.getSearchUser), userController.getSearchUser);

router.route('/image/:userId').get(auth, validate(userValidation.getImageUser), userController.getImageUser);

router
  .route('/:userId')
  .get(auth, validate(userValidation.getUser), userController.getUser)
  .put(auth, upload, validate(userValidation.updateUser), userController.updateUser)
  .delete(auth, validate(userValidation.deleteUser), userController.deleteUser);

module.exports = router;

