const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const blockchainValidation = require('../../validations/blockchain.validation');
const blockchainController = require('../../controllers/blockchain.controller');

const router = express.Router();

router.route('/add-candidate').post(auth, validate(blockchainValidation.addCandidate), blockchainController.addCandidate);
router.route('/set-quota').post(auth, validate(blockchainValidation.setQuota), blockchainController.setQuota);
router.route('/vote').post(auth, validate(blockchainValidation.vote), blockchainController.vote);
router.route('/deploy').post(auth, blockchainController.deploy);
router.route('/candidates').post(auth, blockchainController.getCandidates);

module.exports = router;

